# Define an variable with an integer value
types_of_people = 10
# Define a variable as a string containing the value of another variable
x = "There are #{types_of_people} types of people."
# Define a variable with a string literal
binary = 'binary'
# Define a variable with a string literal; double quotes are required
# due to the apostrophe in the string
do_not = "don't"
# Define a variable as a string containing the values of two other variables
y = "Those who know #{binary} and those who #{do_not}."

# Print the value of variable 'x'
puts x
# Print the value of variable 'y'
puts y

# Print a string containing the value of a variable
puts "I said: #{x}."
# Print a string containing the value of a variable
puts "I also said: '#{y}'."

# Define a variable with a boolean value
hilarious = false
# Define a variable as a string containing the value of another variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# Print the value of a variable
puts joke_evaluation

